public class Theme {

    private String name;
    private String id;
    private int imageViewid;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getImageViewid() {
        return imageViewid;
    }

    public void setImageViewid(int imageViewid) {
        this.imageViewid = imageViewid;
    }
}
